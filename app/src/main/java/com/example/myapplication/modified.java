package com.example.myapplication;

public class modified {
    int smile,comment;


    public modified( int smile, int comment) {

        this.smile = smile;
        this.comment = comment;
    }





    public int getSmile() {
        return smile;
    }

    public void setSmile(int smile) {
        this.smile = smile;
    }

    public int getComment() {
        return comment;
    }

    public void setComment(int comment) {
        this.comment = comment;
    }
}
